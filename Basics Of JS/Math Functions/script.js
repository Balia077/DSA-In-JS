// Math Functions

// 1. Math.round()
// Rounds a number to the nearest integer

let num = 4.7;
console.log(Math.round(num)); // Output: 5

// 2. Math.ceil()
// Rounds a number up to the next largest integer

console.log(Math.ceil(num)); // Output: 5

// 3. Math.floor()
// Rounds a number down to the next smallest integer    

console.log(Math.floor(num)); // Output: 4

// 4. Math.trunc()
// Removes the decimal part of a number, returning only the integer part

console.log(Math.trunc(num)); // Output: 4

// 5. Math.pow()
// Raises a base to the power of an exponent

console.log(Math.pow(2,5)); // Output: 25

// 6. Math.sqrt()
// Returns the square root of a number

console.log(Math.sqrt(16)); // Output: 4

// 7. Math.min()
// Returns the smallest of zero or more numbers

console.log(Math.min(3, 1, 4, 1, 5, 9)); // Output: 1

// 8. Math.max()
// Returns the largest of zero or more numbers

console.log(Math.max(3, 1, 4, 1, 5, 9)); // Output: 9

// 9. Math.random()
// Returns a pseudo-random number between 0 (inclusive) and 1 (exclusive)

console.log(Math.random()); // Output: A random number between 0 and 1

// 10. Math.abs()
// Returns the absolute value of a number

console.log(Math.abs(-7.5)); // Output: 7.5

// 11. Math.sin(), Math.cos(), Math.tan()
// Returns the sine, cosine, and tangent of a number (in radians)

console.log(Math.sin(Math.PI / 2)); // Output: 1
console.log(Math.cos(0));
console.log(Math.tan(Math.PI / 4)); // Output: 1