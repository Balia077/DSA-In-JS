// Math Functions

// 1. Math.round()
// Rounds a number to the nearest integer

let num = 4.7;
console.log(Math.round(num)); // Output: 5

// 2. Math.ceil()
// Rounds a number up to the next largest integer

console.log(Math.ceil(num)); // Output: 5

// 3. Math.floor()
// Rounds a number down to the next smallest integer    

console.log(Math.floor(num)); // Output: 4

// 4. Math.trunc()
// Removes the decimal part of a number, returning only the integer part

console.log(Math.trunc(num)); // Output: 4

// 5. Math.pow()
// Raises a base to the power of an exponent

console.log(Math.pow(2,5)); // Output: 25

